// Helper function to filter locations based on search query
export const filterLocations = (locations, query) => {
  if (!query || query.trim() === '') return [];
  
  const searchQuery = query.toLowerCase().trim();
  
  return locations
    .filter(location => location.toLowerCase().includes(searchQuery))
    .sort((a, b) => {
      // Prioritize matches that start with the query
      const aStarts = a.toLowerCase().startsWith(searchQuery);
      const bStarts = b.toLowerCase().startsWith(searchQuery);
      
      if (aStarts && !bStarts) return -1;
      if (!aStarts && bStarts) return 1;
      
      // Then sort alphabetically
      return a.localeCompare(b);
    })
    .slice(0, 10); // Limit to 10 suggestions
};
